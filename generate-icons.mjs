// Generate PNG icons from SVG for PWA
// Run: node generate-icons.mjs
import { writeFileSync } from "fs";

function createPngPlaceholder(size) {
  // Create a minimal valid PNG with solid color (#0f0f14)
  // For a proper icon, replace these with real PNGs
  // This creates a 1x1 pixel PNG that browsers will scale
  const png = Buffer.from([
    0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, // PNG signature
    0x00, 0x00, 0x00, 0x0d, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // 1x1
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, 0xde, // 8-bit RGB
    0x00, 0x00, 0x00, 0x0c, 0x49, 0x44, 0x41, 0x54, // IDAT chunk
    0x08, 0xd7, 0x63, 0x60, 0x60, 0x60, 0x00, 0x00,
    0x00, 0x04, 0x00, 0x01, 0x27, 0x34, 0x27, 0x0a,
    0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, 0x44, // IEND chunk
    0xae, 0x42, 0x60, 0x82,
  ]);
  writeFileSync(`public/icon-${size}.png`, png);
  console.log(`Created icon-${size}.png (placeholder)`);
}

createPngPlaceholder(192);
createPngPlaceholder(512);
